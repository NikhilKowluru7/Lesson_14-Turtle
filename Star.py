import turtle
turtle.Screen().bgcolor("red")
a = turtle.Turtle()
turtle.Screen().setup(500,500)
a.forward(100)
a.left(120)

a.forward(100)
a.left(120)

a.forward(100)

a.penup()
a.right(150)
a.forward(50)
a.right(90)
a.pendown()

a.forward(100)
a.right(120)

a.forward(100)
a.right(120)

a.forward(100)

turtle.done()